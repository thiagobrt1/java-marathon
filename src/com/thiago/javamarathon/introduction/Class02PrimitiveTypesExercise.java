package com.thiago.javamarathon.introduction;

/*
Create variables for the fields described below between < > and print the following message:
I <name>, living at <address>, confirm that I received the salary of <salary> on <date>.
 */

public class Class02PrimitiveTypesExercise{
    public static void main(String[] args){
        String name = "Thiago";
        String address = "123 Main Street";
        double salary = 2500.00;
        String date = "August 1st, 2025";
        System.out.println("I " + name + ", living at " + address + ", confirm that I received the salary of " + salary + " on " + date + ".");
    }
}
