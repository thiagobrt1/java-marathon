package com.thiago.javamarathon.introduction;

public class Class06Arrays02{
    public static void main(String[] args){
        // Initialization values
        // byte, short, int, long, float, double = 0
        // char = '\u0000' ('')
        // boolean = false
        // Reference types = null

        int[] intValue = new int[1];
        System.out.println(intValue[0]);

        char[] charValue = new char[1];
        System.out.println(charValue[0]);

        boolean[] booleanValue = new boolean[1];
        System.out.println(booleanValue[0]);

        String[] stringValue = new String[1];
        System.out.println(stringValue[0]);
    }
}
